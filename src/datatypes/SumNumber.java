package datatypes;

public class SumNumber {
    public static void main(String[] args) {
        int a = 5;
        int myIntegerFirstNumber = 10;
        int myIntegerSecondNumber = 5000;
        long myLongNumber = 50001;
        long myLongSecondNumber = 50002;
        System.out.println(a);
        System.out.println(myIntegerFirstNumber);
        System.out.println(myIntegerSecondNumber);
        System.out.println(myLongNumber);
        System.out.println(a + myIntegerFirstNumber);
        System.out.println(myIntegerFirstNumber + myIntegerSecondNumber);
        System.out.println(myLongNumber + myLongSecondNumber);
    }
}
